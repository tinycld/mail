const defaultHTTPAddr = "127.0.0.1:7090"

func main() {
	loadEnvFile()

	// Default to port 7090 if --http is not explicitly provided (serve only)
	if hasSubcommand("serve") && !hasFlag("--http") {
		os.Args = append(os.Args, "--http", defaultHTTPAddr)
	}

	if err := sentry.Init(sentry.ClientOptions{
		Dsn:              os.Getenv("SENTRY_DSN"),
		Environment:      getEnvironment(),
		TracesSampleRate: 0.2,
		AttachStacktrace: true,
	}); err != nil {
		log.Printf("Sentry initialization failed: %v", err)
	}
	defer sentry.Flush(2 * time.Second)

	app := pocketbase.New()

	// ---------------------------------------------------------------
	// Optional plugin flags:
	// ---------------------------------------------------------------

	var hooksDir string
	app.RootCmd.PersistentFlags().StringVar(
		&hooksDir,
		"hooksDir",
		"",
		"the directory with the JS app hooks",
	)

	var hooksWatch bool
	app.RootCmd.PersistentFlags().BoolVar(
		&hooksWatch,
		"hooksWatch",
		true,
		"auto restart the app on pb_hooks file change; it has no effect on Windows",
	)

	var hooksPool int
	app.RootCmd.PersistentFlags().IntVar(
		&hooksPool,
		"hooksPool",
		15,
		"the total prewarm goja.Runtime instances for the JS app hooks execution",
	)

	var migrationsDir string
	app.RootCmd.PersistentFlags().StringVar(
		&migrationsDir,
		"migrationsDir",
		"",
		"the directory with the user defined migrations",
	)

	var automigrate bool
	app.RootCmd.PersistentFlags().BoolVar(
		&automigrate,
		"automigrate",
		true,
		"enable/disable auto migrations",
	)

	var publicDir string
	app.RootCmd.PersistentFlags().StringVar(
		&publicDir,
		"publicDir",
		defaultPublicDir(),
		"the directory to serve static files",
	)

	var fallbackFile string
	app.RootCmd.PersistentFlags().StringVar(
		&fallbackFile,
		"fallbackFile",
		"app.html",
		"fallback to this file on missing static path for SPA routes",
	)

	var typesDir string
	app.RootCmd.PersistentFlags().StringVar(
		&typesDir,
		"typesDir",
		defaultTypesDir(),
		"the directory to write generated TypeScript schema files",
	)

	app.RootCmd.ParseFlags(os.Args[1:])

	// ---------------------------------------------------------------
	// Plugins and hooks:
	// ---------------------------------------------------------------

	// load jsvm (pb_hooks and pb_migrations)
	jsvm.MustRegister(app, jsvm.Config{
		MigrationsDir: migrationsDir,
		HooksDir:      hooksDir,
		HooksWatch:    hooksWatch,
		HooksPoolSize: hooksPool,
	})

	// migrate command (with js templates)
	migratecmd.MustRegister(app, app.RootCmd, migratecmd.Config{
		TemplateLang: migratecmd.TemplateLangJS,
		Automigrate:  automigrate,
		Dir:          migrationsDir,
	})

	// Register addon server extensions (generated by scripts/generate-addons.ts)
	registerAddonExtensions(app)

	// ---------------------------------------------------------------
	// Schema generation hooks
	// ---------------------------------------------------------------
	app.OnCollectionCreateRequest().BindFunc(func(e *core.CollectionRequestEvent) error {
		if err := e.Next(); err != nil {
			return err
		}
		generateSchemas(e.App, typesDir)
		return nil
	})

	app.OnCollectionUpdateRequest().BindFunc(func(e *core.CollectionRequestEvent) error {
		if err := e.Next(); err != nil {
			return err
		}
		generateSchemas(e.App, typesDir)
		return nil
	})

	app.OnCollectionDeleteRequest().BindFunc(func(e *core.CollectionRequestEvent) error {
		if err := e.Next(); err != nil {
			return err
		}
		generateSchemas(e.App, typesDir)
		return nil
	})

	// static route to serves files from the provided public dir
	// (if publicDir exists and the route path is not already defined)
	app.OnServe().Bind(&hook.Handler[*core.ServeEvent]{
		Func: func(e *core.ServeEvent) error {
			generateSchemas(e.App, typesDir)

			e.Router.POST("/api/signup", HandleSignup)

			if !e.Router.HasRoute(http.MethodGet, "/{path...}") {
				e.Router.Any("/{path...}", staticWithFallback(publicDir, fallbackFile))
			}

			return e.Next()
		},
		Priority: 999,
	})

	if err := app.Start(); err != nil {
		log.Fatal(err)
	}
}

// the default public dir location is relative to the executable
func defaultPublicDir() string {
	if strings.HasPrefix(os.Args[0], os.TempDir()) {
		// most likely ran with go run
		return "./public"
	}

	return filepath.Join(os.Args[0], "../public")
}

func defaultTypesDir() string {
	if strings.HasPrefix(os.Args[0], os.TempDir()) {
		return "../types"
	}
	return filepath.Join(os.Args[0], "../../types")
}

func hasFlag(name string) bool {
	for _, arg := range os.Args[1:] {
		if arg == name || strings.HasPrefix(arg, name+"=") {
			return true
		}
	}
	return false
}

func hasSubcommand(name string) bool {
	for _, arg := range os.Args[1:] {
		if !strings.HasPrefix(arg, "-") && arg == name {
			return true
		}
	}
	return false
}

func getEnvironment() string {
	if env := os.Getenv("ENVIRONMENT"); env != "" {
		return env
	}
	for _, arg := range os.Args {
		if arg == "--dev" {
			return "development"
		}
	}
	return "production"
}

// staticWithFallback serves static files from dir, falling back to fallbackFile for missing paths
func staticWithFallback(dir string, fallbackFile string) func(*core.RequestEvent) error {
	fs := os.DirFS(dir)

	return func(e *core.RequestEvent) error {
		// Only serve static files for GET/HEAD — let WebDAV methods pass through
		if e.Request.Method != http.MethodGet && e.Request.Method != http.MethodHead {
			return e.Next()
		}

		path := e.Request.PathValue("path")
		if path == "" {
			path = "index.html"
		}

		// Try to open the requested file
		f, err := fs.Open(path)
		if err == nil {
			f.Close()
			return e.FileFS(fs, path)
		}

		// Try path/index.html for directory-style routes (e.g. /features -> features/index.html)
		indexPath := path + "/index.html"
		f, err = fs.Open(indexPath)
		if err == nil {
			f.Close()
			return e.FileFS(fs, indexPath)
		}

		// If fallback is configured and file doesn't exist, serve the fallback
		if fallbackFile != "" {
			return e.FileFS(fs, fallbackFile)
		}

		return e.NotFoundError("", nil)
	}
}

